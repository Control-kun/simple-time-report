    {
        $userId = 'all';

        $users = User::query()
            ->get();

        $reports = Report::query()
            ->orderBy('report_start_date')
            ->get();

        if ($request->user) {
            $userId = $request->user;
            if ($userId !== 'all') {
                $reports = Report::query()
                    ->where('user_id', $userId)
                    ->get();
            }
        }
        return view('statistics.index', compact('users', 'reports'));
    }

